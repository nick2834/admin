suber.config(['$stateProvider',"$locationProvider","$urlRouterProvider", function($stateProvider,$locationProvider,$urlRouterProvider){
    $locationProvider.hashPrefix("");
    $urlRouterProvider.otherwise("/home");
    $stateProvider
        .state('home',{
            url:"/home",
            views:{
                "main":{
                    templateUrl:"./template/admin.html",
                    controller:"home"
                }
            }
        })
        .state('userList',{
            url:"/userList",
            views:{
                "main":{
                    templateUrl:"./template/user/userList.html",
                    controller:"userList"
                }
            }
        })
        .state('userData',{
            url:"/userData/:id",
            views:{
                "main":{
                    templateUrl:"./template/user/userData.html",
                    controller:"userData"
                }
            }
        })
        .state('topic',{
            url:"/topic",
            views:{
                "main":{
                    templateUrl:"./template/include/topicList.html",
                    controller:"topic"
                }
            }
        })
        .state('coupons',{
            url:"/coupons",
            views:{
                "main":{
                    templateUrl:"./template/coupons/coupons.html",
                    controller:"coupons"
                }
            }
        })

}])